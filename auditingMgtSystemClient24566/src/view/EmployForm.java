    private void deleteBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtn1ActionPerformed
        try{
             int response = JOptionPane.showConfirmDialog(null, "Do you really want to delete seleted record", "Confirm", JOptionPane.YES_NO_OPTION);
        if(searchEmpTxt.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Search ID required", "Enter ID", HEIGHT);
        }else 
       if(response==JOptionPane.YES_OPTION){
           Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",6000);
            EmploymentService service =   (EmploymentService) theRegistry.lookup("employment");
            Employment theEmployment = new Employment();
            theEmployment.setId(Integer.parseInt(searchEmpTxt.getText()));

            Employment empObj = service.deleteEmployment(theEmployment);
            if(empObj!=null){
                JOptionPane.showMessageDialog(this, "Data Deleted");
            }else{
                JOptionPane.showMessageDialog(this, "Data not deleted");
            }}
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_deleteBtn1ActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try {
            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",6000);
            EmploymentService service =   (EmploymentService) theRegistry.lookup("employment");
            Employment theEmployment = new Employment();
            theEmployment.setId(Integer.parseInt(searchEmpTxt.getText()));
            Employment theObj = service.searchEmployment(theEmployment);
            if (theObj != null) {
                searchEmpTxt.setText(String.valueOf(theObj.getId()));
                auditorNameCombo.setSelectedItem(theObj.getAuditor());
                institutionCombo.setSelectedItem(theObj.getInstitution());
            } else {
                JOptionPane.showMessageDialog(this, "Data not found");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_searchBtnActionPerformed

