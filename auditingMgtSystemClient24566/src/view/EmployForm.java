    public void addAuditorItemCombo(){
       try {
            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",6000);
           AuditorService service =   (AuditorService) theRegistry.lookup("auditor");
           List<Auditor> auditors = service.allAuditor();
           for(Auditor theObj: auditors){
               auditorNameCombo.addItem(theObj);
           }
       } catch (Exception ex) {
           ex.printStackTrace();
       }  
   }
public void addInstitutionItemCombo(){
       try {
            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",6000);
           InstitutionService service =   (InstitutionService) theRegistry.lookup("institution");
           List<Institution> institutions = service.allInstitution();
           for(Institution theObj: institutions){
               institutionCombo.addItem(theObj);
           }
       } catch (Exception ex) {
           ex.printStackTrace();
       }  
   }
