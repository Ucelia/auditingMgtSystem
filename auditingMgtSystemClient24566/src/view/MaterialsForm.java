    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try {

            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",6000);
            MaterialService service = (MaterialService) theRegistry.lookup("material");
            Materials theMaterial = new Materials();
            theMaterial.setId(Integer.parseInt(searchMaterialTxt.getText()));
            Materials matObj = service.searchMaterial(theMaterial);
            if (matObj != null) {
                searchMaterialTxt.setText(String.valueOf(matObj.getId()));
                nameTxt.setText(matObj.getNames());
                quantityTxt.setText(String.valueOf(matObj.getQuantity()));
            } else {
                //JOptionPane.showMessageDialog(this, "Data not found");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_searchBtnActionPerformed

