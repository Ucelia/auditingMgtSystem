    public void addData() {
    try {
        Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1", 6000);
        MaterialService service = (MaterialService) theRegistry.lookup("material");
        DefaultTableModel model = (DefaultTableModel) materialTable.getModel();
        
        List<Materials> matObj = service.allMaterial();
        
        if (matObj != null && !matObj.isEmpty()) {
            for (Materials mat : matObj) {
                Object data[] = {
                    mat.getId(),
                    mat.getNames(),
                    mat.getQuantity(),
                    mat.getStartedDate()
                };
                model.addRow(data);
            }
        } else {
            //JOptionPane.showMessageDialog(this, "No materials found");
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error fetching materials: " + e.getMessage());
    }
}

    
    
